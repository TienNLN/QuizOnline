package tiennln.servlet;

import java.io.IOException;
import java.io.PrintWriter;
import java.sql.SQLException;
import javax.naming.NamingException;
import javax.servlet.RequestDispatcher;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;
import org.apache.log4j.Logger;
import tiennln.question.QuestionDAO;

/**
 *
 * @author ADMIN
 */
@WebServlet(name = "AddNewQuestionServlet", urlPatterns = {"/AddNewQuestionServlet"})
public class AddNewQuestionServlet extends HttpServlet {

    private final Logger logger = Logger.getLogger(AddNewQuestionServlet.class);

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        PrintWriter out = response.getWriter();

        String txtQuestion = request.getParameter("txtQuestion");
        String txtSubjectID = request.getParameter("txtSubjectID");

        String url = "addNewQuestionPage";

        boolean errorFound = false;

        try {
            if (txtQuestion.trim().isEmpty()) {
                request.setAttribute("QUESTION_EMPTY", "Question cannot be empty !");
                errorFound = true;
            }

            if (!errorFound) {
                QuestionDAO questionDAO = new QuestionDAO();

                boolean added = questionDAO.addNewQuestion(txtQuestion, txtSubjectID);

                if (!added) {
                    request.setAttribute("ADD_QUESTION_FAILED", "Add failed, please try again !");
                } else {
                    HttpSession session = request.getSession();
                    session.setAttribute("QUESTION_ID_ON_TARGET", questionDAO.nextQuestionID() - 1);
                    url = "addNewQuestionChoicePage";
                }
            }
        } catch (NamingException ex) {
            logger.error(ex.getMessage());
            url = "errorPage";
        } catch (SQLException ex) {
            logger.error(ex.getMessage());
            url = "errorPage";
        } finally {
            if (url.equals("errorPage")) {
                response.sendRedirect(url);
            } else {
                RequestDispatcher rd = request.getRequestDispatcher(url);
                rd.forward(request, response);
            }
            out.close();
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
